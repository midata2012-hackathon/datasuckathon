package net.jirasystems.ckan.visualisation;

public class Nodes {

	private Node[] nodes;
	private Link[] links;

	/**
	 * @return the nodes
	 */
	public Node[] getNodes() {
		return nodes;
	}

	/**
	 * @param nodes
	 *            the nodes to set
	 */
	public void setNodes(Node[] nodes) {
		this.nodes = nodes;
	}

	/**
	 * @return the links
	 */
	public Link[] getLinks() {
		return links;
	}

	/**
	 * @param links
	 *            the links to set
	 */
	public void setLinks(Link[] links) {
		this.links = links;
	}
}
